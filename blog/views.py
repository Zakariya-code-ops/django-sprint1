def index(request):
    """Отображает главную страницу блога со списком постов в обратном порядке."""
    # Возвращаем посты в обратном порядке (инвертированный список)
    reversed_posts = posts[::-1]
    return render(request, 'blog/index.html', {'posts': reversed_posts})


def post_detail(request, id):
    """Отображает детальную страницу поста по его ID. Если пост не найден, возвращает страницу ошибки 404."""
    post = next((p for p in posts if p['id'] == id), None)
    if post is None:
        return render(request, '404.html', status=404)
    return render(request, 'blog/detail.html', {'post': post})


def category_posts(request, category_slug):
    """Отображает страницу с постами для указанной категории."""
    return render(request, 'blog/category.html', {'category_slug': category_slug})


